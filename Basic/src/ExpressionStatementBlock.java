public class ExpressionStatementBlock {
    public static void main(String[] args) {
        int number = 1 + 60 + 20 + 19 ;
        // Expression adalah sebuah operasi yang menghasilkan nilai -> 1 + 60 + 20 + 19
        // Statement berisikan execution komplit, biasanya diakhiri dengan titik koma (contohnya inisialisasi variable)
        System.out.println(number = 100);
    }
    // Block adalah grup dari satu atau lebih statement yang diapit oleh kurung kurawal {}.
}
